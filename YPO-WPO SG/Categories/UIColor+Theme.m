#import "UIColor+Theme.h"

@implementation UIColor (Theme)

+ (UIColor *)colorBlueTheme {
    return UIColorFromRGB(0x034784);
}


+ (UIColor *)colorGoldTheme {
    return UIColorFromRGB(0xbb9a6a);
}


+ (UIColor *)colorDefaultTheme {
    return UIColorFromRGB(0x222222);
}

@end
